public class MultiplicaAi {
    public static void main(String[] args) {
        int x = 5;
        System.out.println("Tabela de multiplicação por " + x);
        for(int i = 1; i<=10; i++){
            System.out.println(x + " x " + i + " = " + i*x);
        }
    }
}
